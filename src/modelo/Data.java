package modelo;

/**
 * Classe responsável pelo controle de DATA
 * @author Diego
 * @since 06/02/2014
 *
 */

public class Data {//inicio da classe
	
	private int dia;
	private int mes;
	private int ano;
	
	public int getDia() {
		return dia;
	}
	public void setDia(int dia) {
		this.dia = dia;
	}
	public int getMes() {
		return mes;
	}
	public void setMes(int mes) {
		this.mes = mes;
	}
	public int getAno() {
		return ano;
	}
	public void setAno(int ano) {
		this.ano = ano;
	}
	
}//fim da classe